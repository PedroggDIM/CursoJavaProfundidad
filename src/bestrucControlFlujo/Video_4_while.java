public class Video_4_while {

	public static void main(String[] args) {
		
		   int x=0;
		   
		   // Mientras (la expresión booleana sea verdadera)
		  //se cumple el bloque
		while(x<=100) {
        	System.out.println(x);
        	x++;
        }
		System.out.println("Fin de la ejecución");
	}

}
