package bestrucControlFlujo;

public class Video_9_switch_String {

	public static void main(String[] args) {

		String opcion = "uno";

		switch (opcion) {
		case "cero":
			System.out.println("Opción cero");
			break;
		case "uno":
			System.out.println("Opción uno");
			break;
		case "dos":
			System.out.println("Opción dos");
			break;
		case "tres":
			System.out.println("Opción tres");
			break;
		default:
			System.out.println("Opción cuatro");
			break;
		}

	}

}
