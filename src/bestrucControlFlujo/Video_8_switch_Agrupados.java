package bestrucControlFlujo;

public class Video_8_switch_Agrupados {

	public static void main(String[] args) {
		
		int opcion = 0;

		switch (opcion) {
		case 0:
		case 1:	
		case 2:	
			System.out.println("Opción 0, 1, 2");		
			break;
		case 3:
			System.out.println("Opción 3");
			break;
		default:
			System.out.println("Opción 4");
			break;
		}

	}

}
